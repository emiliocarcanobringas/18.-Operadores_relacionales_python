diferente = 9 != 10 # Planteamos una diferencia cuyo valor es verdadero
print(diferente) # Imprimimos el valor de diferente

menorOigual = 4 <= 12 #Se propone una comparación o igualdad, se cumple la comparación con valor verdadero
print (menorOigual) # Imprimimos en pantalla el valor de menorOigual

mayorOigual = 32 >= 32 # Aquí se cumple la igualdad
print (mayorOigual) # Imprimimos el valor de mayorOigual

